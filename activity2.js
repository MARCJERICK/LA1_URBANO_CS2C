// Declare a Variable for the first and second favorite number
let firstfavnum = 16;
let secondfavnum = 19;

// Perform addition of the two numbers and store the result
let add = firstfavnum + secondfavnum;
// Perform subtraction of the two numbers and store the result
let sub = firstfavnum - secondfavnum;
// Perform multiplication of the two numbers and store the result
let mult = firstfavnum * secondfavnum;
// Perform division of the two numbers and store the result
let div = firstfavnum / secondfavnum;
// Perform modulus (remainder of division) operation and store the result
let mod = firstfavnum % secondfavnum;
// Perform exponentiation (firstfavnum raised to the power of secondfavnum) and store the result
let power = firstfavnum ** secondfavnum;
// Log the result of all the operation
console.log(add);
console.log(sub);
console.log(mult);
console.log(div);
console.log(mod);
console.log(power);